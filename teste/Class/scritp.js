class Pessoa {
    constructor(nome, idade){
        this.nome = nome;
        this.idade = idade;
    }
}

const pessoa1 = new Pessoa("julio", 19);
const pessoa2 = new Pessoa("ana", 22);
const pessoa3 = new Pessoa("lucas", 29);
const pessoa4 = new Pessoa("beatriz", 17);

console.log(pessoa1.idade);
console.log(pessoa2.nome);
console.log(pessoa3);
console.log(pessoa4);

